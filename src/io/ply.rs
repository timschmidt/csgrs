#![doc = ""]
#![doc = " This module provides export functionality for Stanford PLY files,"]
#![doc = " a popular format for 3D scanning, research, and mesh processing applications."]

use crate::triangulated::IndexedTriangulated3D;
use crate::vertex::Vertex;
use hashbrown::HashMap;
use std::fmt::Debug;
use std::io::Write;

use super::{IoError, finite_f64, single_line_metadata};

fn checked_face_index(index: usize) -> Result<i32, IoError> {
    i32::try_from(index).map_err(|_| IoError::SizeOverflow {
        format: "PLY",
        limit: "signed 32-bit face index",
    })
}

fn build_ply_buffers<T: IndexedTriangulated3D>(
    shape: &T,
) -> Result<(Vec<Vertex>, Vec<[i32; 3]>), IoError> {
    let indexed = shape.indexed_triangles();
    let mut vertices = Vec::new();
    let mut vertex_map = HashMap::<(usize, usize), usize>::new();
    let faces = indexed
        .faces
        .into_iter()
        .map(|face| {
            face.into_iter()
                .map(|key @ (position, normal)| {
                    if position >= indexed.positions.len() || normal >= indexed.normals.len() {
                        return Err(IoError::Geometry {
                            format: "PLY",
                            detail: "indexed triangle references missing vertex data".into(),
                        });
                    }
                    let index = *vertex_map.entry(key).or_insert_with(|| {
                        let index = vertices.len();
                        vertices.push(Vertex {
                            position: indexed.positions[position].clone(),
                            normal: indexed.normals[normal].clone(),
                        });
                        index
                    });
                    checked_face_index(index)
                })
                .collect::<Result<Vec<_>, _>>()?
                .try_into()
                .map_err(|_| IoError::Geometry {
                    format: "PLY",
                    detail: "triangle did not contain exactly three indices".into(),
                })
        })
        .collect::<Result<Vec<_>, IoError>>()?;
    Ok((vertices, faces))
}

#[doc = " Export any `Triangulated3D` shape to PLY format as a string"]
#[doc = ""]
#[doc = " Creates a Stanford PLY file containing all triangles from `shape`."]
#[doc = ""]
#[doc = " # Arguments"]
#[doc = " * `comment` - Optional comment to include in PLY header"]
pub fn to_ply<T: IndexedTriangulated3D>(shape: &T, comment: &str) -> Result<String, IoError> {
    let comment = single_line_metadata(comment, "PLY", "comment")?;
    let (vertices, faces) = build_ply_buffers(shape)?;

    let mut ply_content = String::new();
    ply_content.push_str("ply\n");
    ply_content.push_str("format ascii 1.0\n");
    ply_content.push_str(&format!("comment {comment}\n"));
    ply_content.push_str("comment Generated by csgrs library\n");
    ply_content.push_str(&format!("element vertex {}\n", vertices.len()));
    ply_content.push_str("property double x\n");
    ply_content.push_str("property double y\n");
    ply_content.push_str("property double z\n");
    ply_content.push_str("property double nx\n");
    ply_content.push_str("property double ny\n");
    ply_content.push_str("property double nz\n");
    ply_content.push_str(&format!("element face {}\n", faces.len()));
    ply_content.push_str("property list uchar int vertex_indices\n");
    ply_content.push_str("end_header\n");

    for vertex in &vertices {
        ply_content.push_str(&format!(
            "{:.17} {:.17} {:.17} {:.17} {:.17} {:.17}\n",
            finite_f64(&vertex.position.x, "PLY", "vertex x")?,
            finite_f64(&vertex.position.y, "PLY", "vertex y")?,
            finite_f64(&vertex.position.z, "PLY", "vertex z")?,
            finite_f64(&vertex.normal.0[0], "PLY", "normal x")?,
            finite_f64(&vertex.normal.0[1], "PLY", "normal y")?,
            finite_f64(&vertex.normal.0[2], "PLY", "normal z")?
        ));
    }

    for face in &faces {
        ply_content.push_str(&format!("3 {} {} {}\n", face[0], face[1], face[2]));
    }

    Ok(ply_content)
}

#[doc = " Export any `Triangulated3D` shape to a PLY file"]
#[doc = ""]
#[doc = " # Arguments"]
#[doc = " * `writer` - Where to write the PLY data"]
#[doc = " * `comment` - Comment to include in PLY header"]
pub fn write_ply<T: IndexedTriangulated3D, W: Write>(
    shape: &T,
    writer: &mut W,
    comment: &str,
) -> Result<(), IoError> {
    let ply_content = to_ply(shape, comment)?;
    writer.write_all(ply_content.as_bytes())?;
    Ok(())
}

impl<M: Clone + Debug + Send + Sync> crate::mesh::Mesh<M> {
    #[doc = " Export this Mesh to PLY format as a string"]
    pub fn to_ply(&self, comment: &str) -> Result<String, IoError> {
        self::to_ply(self, comment)
    }

    #[doc = " Export this Mesh to a PLY file"]
    pub fn write_ply<W: Write>(&self, writer: &mut W, comment: &str) -> Result<(), IoError> {
        self::write_ply(self, writer, comment)
    }
}

